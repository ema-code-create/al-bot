# 投稿時刻スロット(JST): 5, 12, 16, 19, 23時
SLOTS = [5, 12, 16, 19, 23]


def pick_tweet() -> str:
    now_jst = datetime.datetime.now(datetime.timezone(datetime.timedelta(hours=9)))
    today = now_jst.date()
    hour = now_jst.hour

    # 今日のスロット番号(直近のスロットを選ぶ)
    slot_index = max(
        (i for i, h in enumerate(SLOTS) if h <= hour),
        default=len(SLOTS) - 1,
    )

    # 日付をシードにシャッフルして同日内の5スロットは必ず異なるツイートを選ぶ
    rng = random.Random(today.toordinal())
    shuffled = list(range(len(TWEETS)))
    rng.shuffle(shuffled)
    return TWEETS[shuffled[slot_index % len(shuffled)]]


def main():
    tweet_text = pick_tweet()
    response = client.create_tweet(text=tweet_text)
    print(f"投稿完了: {tweet_text[:30]}... (id: {response.data['id']})")


if __name__ == "__main__":
    main()
